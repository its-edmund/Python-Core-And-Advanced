# Integer Types

a=13
b=100
c=-66
print(a,b,c)
print(type(a))

# Floating Types

x=33.5
y=-25.8
z=285.0
print(x,y,z)
print(type(z))

# Complex Types

d=3+5j
print(d)
print(type(d))

# Binary Types

e=0b1010
print(e)
print(type(e))

# Hexadecimal Types

f=0XFF
print(f)
print(type(f))

# Boolean Types

g=True
print(g)
print(type(g))
print(9>8)

# Type Conversion

h=int(x)
print(h)
print(type(h))

i=float('22.5')
print(i)
print(type(i))

print(bin(10))
print(oct(10))
print(hex(10))